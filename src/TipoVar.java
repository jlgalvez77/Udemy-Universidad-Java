public class TipoVar {
    public static void main(String[] args) {
        // Sin el uso de var
        String nombre1 = "José";
        System.out.println("nombre1 = " + nombre1);
        // Con el uso de var
        var nombre2 = "Luis";
        // Cuando se define una variable con var, hay que asignarle automáticamente su valor
    }
}
